import { datetimeFormat } from '@/utils/utils.ts'
import type { ChatMessage } from '@/api/chat.ts'
import { chatApi } from '@/api/chat.ts'
import { ref } from 'vue'
import { ElMessage } from 'element-plus'

const props = defineProps<{
  message: ChatMessage
}>()

const sqlFeedback = ref(props.message?.record?.sql_feedback)
const chartFeedback = ref(props.message?.record?.chart_feedback)

const handleFeedback = async (type: 'like' | 'dislike', logType: 'sql' | 'chart') => {
  try {
    const logId =
      logType === 'sql' ? props.message?.record?.sql_log_id : props.message?.record?.chart_log_id

    if (!logId) {
      console.warn('No log_id found for feedback')
      return
    }

    await chatApi.updateFeedback(logId, type)

    if (logType === 'sql') {
      sqlFeedback.value = type
      if (props.message?.record) {
        props.message.record.sql_feedback = type
      }
    } else {
      chartFeedback.value = type
      if (props.message?.record) {
        props.message.record.chart_feedback = type
      }
    }

    ElMessage.success('反馈已提交')
  } catch (error) {
    console.error('Failed to submit feedback:', error)
    ElMessage.error('反馈提交失败')
  }
}
